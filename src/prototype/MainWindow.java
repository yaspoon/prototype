    private boolean login()
    {
        if(!loggedIn)
        {
            loginDialog.setSize(333, 215);
            errorMsg.setVisible(false);
            passwordErrorLabel.setVisible(false);
            usernameIncorrectIcon.setVisible(false);
            loginDialog.setVisible(true);
        }
        
        return loggedIn;
    }
    
